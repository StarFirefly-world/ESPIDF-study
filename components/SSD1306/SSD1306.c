// 初始化I2C
esp_err_t ssd1306_init(i2c_port_t i2c_num, uint8_t sda_pin, uint8_t scl_pin)
{
    i2c_master_bus_config_t bus_config = {
        .i2c_port = i2c_num,
        .sda_io_num = sda_pin,
        .scl_io_num = scl_pin,
        .clk_source = I2C_CLK_SRC_DEFAULT,
        .glitch_ignore_cnt = 7,
        .flags.enable_internal_pullup = true,
    };
    
    ESP_ERROR_CHECK(i2c_new_master_bus(&bus_config, &g_i2c_bus));
    
    i2c_device_config_t dev_config = {
        .dev_addr_length = I2C_ADDR_BIT_LEN_7,
        .device_address = SSD1306_I2C_ADDR,
        .scl_speed_hz = 400000,
    };
    
    ESP_ERROR_CHECK(i2c_master_bus_add_device(g_i2c_bus, &dev_config, &g_i2c_dev));
    
    // 初始化SSD1306
    ssd1306_send_command(SSD1306_CMD_DISPLAY_OFF);
    ssd1306_send_command(SSD1306_CMD_SET_DISPLAY_CLOCK_DIVIDE);
    ssd1306_send_command(0x80);
    ssd1306_send_command(SSD1306_CMD_SET_MULTIPLEX_RATIO);
    ssd1306_send_command(0x3F);
    ssd1306_send_command(SSD1306_CMD_SET_DISPLAY_OFFSET);
    ssd1306_send_command(0x00);
    ssd1306_send_command(SSD1306_CMD_SET_START_LINE);
    ssd1306_send_command(SSD1306_CMD_SET_CHARGE_PUMP);
    ssd1306_send_command(0x14);
    ssd1306_send_command(SSD1306_CMD_SET_MEMORY_ADDRESSING_MODE);
    ssd1306_send_command(0x00);
    
    // 横向模式设置
    ssd1306_send_command(SSD1306_CMD_SET_SEGMENT_REMAP_REVERSE);
    ssd1306_send_command(SSD1306_CMD_SET_COM_SCAN_DIRECTION_REVERSE);
    
    ssd1306_send_command(SSD1306_CMD_SET_PRECHARGE_PERIOD);
    ssd1306_send_command(0xF1);
    ssd1306_send_command(SSD1306_CMD_SET_VCOMH_DESELECT_LEVEL);
    ssd1306_send_command(0x40);
    ssd1306_send_command(SSD1306_CMD_SET_NORMAL_DISPLAY);
    ssd1306_send_command(SSD1306_CMD_DISPLAY_ON);
    
    ssd1306_clear_screen();
    return ESP_OK;
}

// 发送命令
esp_err_t ssd1306_send_command(uint8_t cmd)
{
    uint8_t data[2] = {0x00, cmd};
    return i2c_master_transmit(g_i2c_dev, data, sizeof(data), pdMS_TO_TICKS(100));
}

// 发送数据
esp_err_t ssd1306_send_data(uint8_t* data, size_t len)
{
    uint8_t* buffer = (uint8_t*)malloc(len + 1);
    if (!buffer) return ESP_ERR_NO_MEM;
    
    buffer[0] = 0x40;
    memcpy(buffer + 1, data, len);
    
    esp_err_t ret = i2c_master_transmit(g_i2c_dev, buffer, len + 1, pdMS_TO_TICKS(100));
    free(buffer);
    return ret;
}

// 清除屏幕
esp_err_t ssd1306_clear_screen(void)
{
    memset(g_frame_buffer, 0, sizeof(g_frame_buffer));
    return ssd1306_update_screen();
}

// 绘制像素
esp_err_t ssd1306_draw_pixel(uint8_t x, uint8_t y, uint8_t color)
{
    if (x >= SSD1306_WIDTH || y >= SSD1306_HEIGHT) return ESP_ERR_INVALID_ARG;
    
    uint8_t page = y / 8;
    uint8_t bit = y % 8;
    
    if (color) {
        g_frame_buffer[page * SSD1306_WIDTH + x] |= (1 << bit);
    } else {
        g_frame_buffer[page * SSD1306_WIDTH + x] &= ~(1 << bit);
    }
    
    return ESP_OK;
}

// 更新屏幕
esp_err_t ssd1306_update_screen(void)
{
    ssd1306_send_command(SSD1306_CMD_SET_COLUMN_ADDRESS);
    ssd1306_send_command(0x00);
    ssd1306_send_command(0x7F);
    ssd1306_send_command(SSD1306_CMD_SET_PAGE_ADDRESS);
    ssd1306_send_command(0x00);
    ssd1306_send_command(0x07);
    
    return ssd1306_send_data(g_frame_buffer, sizeof(g_frame_buffer));
}

// 绘制字符串
esp_err_t ssd1306_draw_string(uint8_t x, uint8_t y, const char* str, uint8_t size)
{
    for (int i = 0; str[i]; i++) {
        // 确保字符在字体库范围内
        if (str[i] < ' ' || str[i] > 'z') {
            continue;
        }
        
        // 计算字符在字体库中的索引
        int char_index = (str[i] - ' ') * 8;
        
        for (int j = 0; j < 8; j++) {
            uint8_t row = font_8x8[char_index + j];
            for (int k = 0; k < 8; k++) {
                uint8_t pixel = (row >> (7 - k)) & 1;
                ssd1306_draw_pixel(x + i * 8 + j, y + (7 - k), pixel);
            }
        }
    }
    return ssd1306_update_screen();
}

// 清理I2C资源
esp_err_t ssd1306_deinit(void)
{
    if (g_i2c_dev) {
        ESP_ERROR_CHECK(i2c_master_bus_rm_device(g_i2c_dev));
        g_i2c_dev = NULL;
    }
    
    if (g_i2c_bus) {
        ESP_ERROR_CHECK(i2c_del_master_bus(g_i2c_bus));
        g_i2c_bus = NULL;
    }
    
    return ESP_OK;
}